# Функция для преобразования строки в число с плавающей точкой с обработкой исключений
def convert_to_float(user_input: str) -> float:
    try:
        # Попытка преобразовать строку в число с плавающей точкой
        return float(user_input)
    except ValueError:
        # Обработка ошибки, если строку невозможно преобразовать в число
        raise ValueError(f"Ошибка: '{user_input}' невозможно преобразовать в число с плавающей точкой.")


# Основная программа
def main() -> None:
    try:
        # Запрос строки у пользователя
        user_input: str = input("Введите строку для преобразования в число с плавающей точкой: ")

        # Преобразование строки в число с плавающей точкой
        result: float = convert_to_float(user_input)

        # Вывод результата
        print(f"Преобразованное число: {result}")

    # Обработка ошибок некорректного ввода (когда строку невозможно преобразовать)
    except ValueError as e:
        print(e)


if __name__ == "__main__":
    main()
